// Switch statement
let a = 420;

switch (a) {
  case 42:
    console.log('life');
    break;
  case 777:
    console.log('luck');
    break;
  default:
    console.log('meh');
    break;
}

// If statement
if (a === 42) {
  console.log('life');
} else if (a === 777) {
  console.log('luck');
} else {
  console.log('meh');
}

